# BAND NAME GENERATOR 
# Problem: create a band name generator taking city name and pet name from the user , and join that two strings to create a final band name
print("Welcome to Band Name Generator :) ")
city=input("Enter the name of your city you grewup in :\n")
pet=input("Enter the name of your pet:\n")
Band_name=city+" "+pet
print("YOUR BAND NAME COULD BE : ",Band_name)

